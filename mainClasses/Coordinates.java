



public class Coordinates{

   private int x;
   private int y;
   private int h;
   private int w;
   
   public Coordinates(int x, int y, int h, int w){
      this.x = x;
      this.y = y;
      this.h = h;
      this.w = w;
      
   
   }
   
  
   
   public Coordinates(){
   }
   
   
   //functions
   
   public int getX(){
      return x;
   }
   
   
   public int getY(){
      return y;
   }


   public int getH(){
      return h;
   }


   public int getW(){
      return w;
   }
















} 